/** Bake everything. Call once in PreloadScene.create(). */
export function bakeArt(scene: Phaser.Scene, px = 1): void {
  bakeSprite(scene, Tex.WarIdle0, WAR_IDLE0, { px });
  bakeSprite(scene, Tex.WarIdle1, WAR_IDLE1, { px });
  bakeSprite(scene, Tex.WarWalk0, WAR_WALK0, { px });
  bakeSprite(scene, Tex.WarWalk1, WAR_WALK1, { px });
  bakeSprite(scene, Tex.WarWalk2, WAR_WALK2, { px });
  bakeSprite(scene, Tex.WarWalk3, WAR_WALK3, { px });
  bakeSprite(scene, Tex.WarJump, WAR_JUMP, { px });
  bakeSprite(scene, Tex.WarAtk0, WAR_ATK0, { px });
  bakeSprite(scene, Tex.WarAtk1, WAR_ATK1, { px });
  bakeMagician(scene, px);
  bakeEnemies(scene, px);
  bakeTilesAndFx(scene, px);
}
